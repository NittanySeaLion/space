function sunPos(jd) {
  const n = jd - 2451545.0, L = n360(280.460 + .9856474 * n), g = (357.528 + .9856003 * n) * D2R;
  const lam = (L + 1.915 * sin(g) + .020 * sin(2 * g)) * D2R, eps = 23.439 * D2R;
  return { ra: n360(atan2(cos(eps) * sin(lam), cos(lam)) * R2D), dec: asin(sin(eps) * sin(lam)) * R2D, dist: 1.0 };
}

