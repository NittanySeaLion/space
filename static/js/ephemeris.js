// ── Earth position as seen from Moon ────────────────────────────────────────
function earthFromMoon(jd) {
  const moon = moonPos(jd);
  return { ra: n360(moon.ra + 180), dec: -moon.dec, dist: moon.dist, phase: moon.phase, earthAngSize: 1.9 };
}

// ── Lunar Local Sidereal Time ───────────────────────────────────────────────
// Moon is tidally locked: its prime meridian (lon=0) faces Earth.
// "LST" at selenographic longitude λ = RA_of_Earth_from_Moon + λ
// = moonRA + 180° + λ  (Earth is opposite the Moon in RA)
// This rotates ~13°/day (Moon's orbital period) instead of 360°/day.
let _moonRACache = { jd: 0, ra: 0 };
function lunarLST(jd, lonDeg) {
  if (abs(jd - _moonRACache.jd) > 0.0001) {  // recalc every ~8 sec
    _moonRACache = { jd, ra: moonPos(jd).ra };
  }
  return n360(_moonRACache.ra + 180 + lonDeg);
}

// ── Alt/Az from selenographic coords ────────────────────────────────────────
function altaz(ra, dec, lat, lst) {
  const ha = n360(lst - ra), haR = ha * D2R, decR = dec * D2R, latR = lat * D2R;
  const sinAlt = sin(decR)*sin(latR) + cos(decR)*cos(latR)*cos(haR);
  const alt = asin(Math.max(-1, Math.min(1, sinAlt))) * R2D;
  const cosAz = (sin(decR) - sin(alt * D2R)*sin(latR)) / (cos(alt * D2R)*cos(latR) + 1e-12);
  let az = acos(Math.max(-1, Math.min(1, cosAz))) * R2D;
  if (sin(haR) > 0) az = 360 - az;
  return { alt, az };
}

