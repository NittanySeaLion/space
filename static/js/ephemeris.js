function vseries(a, tau) {
  let s = 0;
  for (let i = 0; i < a.length; i += 3) s += a[i] * cos(a[i+1] + a[i+2] * tau);
  return s;
}

function helio(nm, tau) {
  const d = E[nm];
  const L = (vseries(d.L0, tau) + vseries(d.L1, tau) * tau + vseries(d.L2, tau) * tau * tau) / 1e8;
  const B = vseries(d.B0, tau) / 1e8;
  const R = (vseries(d.R0, tau) + vseries(d.R1, tau) * tau) / 1e8;
  return { x: R * cos(B) * cos(L), y: R * cos(B) * sin(L), z: R * sin(B) };
}

function geocentric(nm, jd) {
  const tau = (jd - 2451545.0) / 365250.0;
  const earth = helio('Earth', tau), body = helio(nm, tau);
  let dx = body.x - earth.x, dy = body.y - earth.y, dz = body.z - earth.z;
  const dist0 = sqrt(dx*dx + dy*dy + dz*dz);
  const tau2 = (jd - dist0 * 0.0057755183 - 2451545.0) / 365250.0;
  const b2 = helio(nm, tau2);
  dx = b2.x - earth.x; dy = b2.y - earth.y; dz = b2.z - earth.z;
  const eps = 23.4393 * D2R;
  const xe = dx, ye = dy * cos(eps) - dz * sin(eps), ze = dy * sin(eps) + dz * cos(eps);
  const dist = sqrt(xe*xe + ye*ye + ze*ze);
  return { ra: n360(atan2(ye, xe) * R2D), dec: asin(Math.max(-1, Math.min(1, ze / dist))) * R2D, dist };
}

