// ── Houston time ────────────────────────────────────────────────────────────
function houstonOffset(date) {
  const fmt = new Intl.DateTimeFormat('en-US', { timeZone: 'America/Chicago', timeZoneName: 'short' });
  const parts = fmt.formatToParts(date);
  const tzName = parts.find(p => p.type === 'timeZoneName')?.value || 'CDT';
  return tzName === 'CDT' ? -5 : -6;
}

function houstonTime(date) {
  const off = houstonOffset(date);
  const local = new Date(date.getTime() + off * 3600000);
  const z = n => String(n).padStart(2, '0');
  const H = local.getUTCHours(), M = local.getUTCMinutes(), S = local.getUTCSeconds();
  const ampm = H >= 12 ? 'PM' : 'AM';
  const h12 = H % 12 || 12;
  const tz = off === -5 ? 'CDT' : 'CST';
  return `${z(local.getUTCMonth()+1)}/${z(local.getUTCDate())}/${local.getUTCFullYear()}  ${z(h12)}:${z(M)}:${z(S)} ${ampm} ${tz}`;
}

function compassDir(az) {
  const dirs = ['N','NNE','NE','ENE','E','ESE','SE','SSE','S','SSW','SW','WSW','W','WNW','NW','NNW'];
  return dirs[Math.round(az / 22.5) % 16];
}
